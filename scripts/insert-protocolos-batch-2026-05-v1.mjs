// Validaciones rápidas
let warnings = 0;
const EMOJI_RE = /[\u{1F300}-\u{1F6FF}\u{2600}-\u{27BF}\u{1F900}-\u{1F9FF}]/u;
for (const t of ALL_TOPICS) {
  // protocol_authors presente
  if (!t.protocol_authors?.length) {
    console.warn(`  WARN: ${t.protocol_code} sin protocol_authors`);
    warnings++;
  }
  // todos los bloques con tab
  for (const b of t.content_blocks) {
    if (!b.tab) {
      console.warn(`  WARN: ${t.protocol_code} bloque ${b.id} sin tab`);
      warnings++;
    }
    // sin emojis decorativos en items/details/title (excluye telefono ☎ 📞 📱)
    const blob = JSON.stringify({ items: b.items, details: b.details, title: b.title, content: b.content });
    if (EMOJI_RE.test(blob)) {
      // No fail, solo aviso
    }
  }
}

// Resumen por topic
ALL_TOPICS.forEach(t => {
  const tabs = new Set(t.content_blocks.map(b => b.tab).filter(Boolean));
  console.log(`  [${t.protocol_code}] ${t.name.slice(0, 55)}`);
  console.log(`     id: ${t.id}`);
  console.log(`     bloques: ${t.content_blocks.length}  |  pestañas: ${[...tabs].join(', ')}`);
});
console.log(`\n  TOTAL: ${ALL_TOPICS.length} topics, ${warnings} warnings\n`);

if (!APPLY) {
  console.log('Modo dry-run. Agregá --apply para escribir en la base de datos.');
  process.exit(0);
}

// Verificar que no existan duplicados por protocol_code
const codes = ALL_TOPICS.map(t => t.protocol_code);
const { data: existing } = await supabase
  .from('topics').select('id, protocol_code').in('protocol_code', codes);

if (existing && existing.length > 0) {
  console.error(`\nERROR: Ya existen topics con estos codes:`);
  existing.forEach(e => console.error(`  ${e.protocol_code} — id ${e.id}`));
  console.error('Para reinsertar, primero eliminá los existentes manualmente.');
  process.exit(1);
}

// Insertar uno por uno para reportar errores específicos
let ok = 0;
for (const t of ALL_TOPICS) {
  const payload = { ...t, last_updated: TODAY, published_date: TODAY.slice(0, 10) };
  const { error } = await supabase.from('topics').insert(payload);
  if (error) {
    console.error(`  ❌ ${t.protocol_code}: ${error.message}`);
  } else {
    ok++;
    console.log(`  ✅ ${t.protocol_code} insertado`);
  }
}
console.log(`\nInsertados: ${ok}/${ALL_TOPICS.length}`);

